#=============================================================
#================       SERVER       =========================
#=============================================================
from flask import Flask, render_template
import random


#=============================================================
#=====     START AND CONFIGURE THE WEB APP     ===============
#=============================================================
WebApp = Flask(
			__name__, 
			static_folder = "../Static/Distribution", 
			template_folder = "../Static"
		)

WebApp.config['SEND_FILE_MAX_AGE_DEFAULT'] = 0
WebApp.config['TEMPLATES_AUTO_RELOAD'] = True


#=============================================================
#================           ROUTES      ======================
#=============================================================

#=================================================
#==========     ROUT: INDEX       ================
#=================================================
@WebApp.route("/")
def index():
    return render_template("index.html")


#=================================================
#==========     ROUT: HELLO       ================
#=================================================
@WebApp.route("/hello")
def hello():
    return get_hello()

def get_hello():
    greeting_list = ["Lalo", "Daniel", "Men de maestria"]
    return random.choice(greeting_list)




#=============================================================
#================           MAIN        ======================
#=============================================================
if __name__ == "__main__":
    WebApp.run(host='10.100.75.199')

